CREATE OR REPLACE TRIGGER FoneClinicaFK
  BEFORE INSERT ON FONE_CLINICA
  FOR EACH ROW
DECLARE
  value CLINICA.CPF%TYPE;
BEGIN
  SELECT CPF INTO value 
  FROM CLINICA
  WHERE CPF NOT IN (
    SELECT CPF 
    FROM FONE_CLINICA
  ) AND ROWNUM = 1;
  :NEW.CPF := value;
END;
