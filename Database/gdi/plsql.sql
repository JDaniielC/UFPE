CREATE OR REPLACE TRIGGER FoneClinicaFK
  BEFORE INSERT ON FONE_CLINICA
  FOR EACH ROW
DECLARE
  value CLINICA.ID%TYPE;
BEGIN
  SELECT ID INTO value FROM
    ( SELECT ID FROM CLINICA
    ORDER BY dbms_random.value )
    WHERE rownum = 1

 :NEW.ID := value;
END;
/

